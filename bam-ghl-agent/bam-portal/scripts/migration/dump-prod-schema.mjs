// Dump the production database schema via the Supabase Management API.
//
// Same auth approach as apply-pending-sql.mjs: an account-level personal
// access token (sbp_...) queries the portal project's catalog. Read-only —
// runs only SELECTs against pg_catalog/information_schema.
//
// Output: supabase/snapshots/prod-schema.sql — a deterministic, diffable snapshot of
// every user schema: tables (with columns/defaults), constraints, indexes,
// RLS status + policies, views, functions, triggers, and enums. Commit the
// file; re-run any time to see what changed in prod since the last dump.
//
// Usage:  node scripts/migration/dump-prod-schema.mjs <sbp_... token>
// Token:  https://supabase.com/dashboard/account/tokens

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const PAT = process.argv[2];
if (!PAT || !PAT.startsWith("sbp_")) {
  console.error("Usage: node dump-prod-schema.mjs <sbp_... Supabase access token>");
  console.error("Create one (account-level) at https://supabase.com/dashboard/account/tokens");
  process.exit(1);
}

const REF = process.env.SUPABASE_PROJECT_REF || "jnojmfmpnsfmtqmwhopz";
const here = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(here, "..", "..", "supabase", "snapshots", "prod-schema.sql");

// Schemas to snapshot. auth/storage/realtime etc. are Supabase-managed.
const SCHEMAS = "'public', 'fc'";

async function q(sql) {
  const r = await fetch(`https://api.supabase.com/v1/projects/${REF}/database/query`, {
    method: "POST",
    headers: { Authorization: `Bearer ${PAT}`, "Content-Type": "application/json" },
    body: JSON.stringify({ query: sql }),
  });
  const text = await r.text();
  if (!r.ok) throw new Error(`Management API ${r.status}: ${text.slice(0, 400)}`);
  return JSON.parse(text);
}

const section = (title) => `\n-- ============================================================\n-- ${title}\n-- ============================================================\n`;

async function main() {
  let out = `-- Production schema snapshot — project ${REF}\n`;
  out += `-- Generated by scripts/migration/dump-prod-schema.mjs (read-only catalog queries)\n`;
  out += `-- NOTE: reconstructed from pg_catalog; close to, but not byte-identical with, pg_dump output.\n`;

  // ── Enums ──────────────────────────────────────────────────────────────
  const enums = await q(`
    select n.nspname as schema, t.typname as name,
           string_agg(quote_literal(e.enumlabel), ', ' order by e.enumsortorder) as labels
    from pg_type t
    join pg_enum e on e.enumtypid = t.oid
    join pg_namespace n on n.oid = t.typnamespace
    where n.nspname in (${SCHEMAS})
    group by 1, 2 order by 1, 2;`);
  out += section("ENUM TYPES");
  for (const e of enums) out += `CREATE TYPE ${e.schema}.${e.name} AS ENUM (${e.labels});\n`;

  // ── Tables: columns + RLS flag ─────────────────────────────────────────
  const cols = await q(`
    select c.relnamespace::regnamespace::text as schema, c.relname as table,
           c.relrowsecurity as rls_enabled, a.attname as column,
           format_type(a.atttypid, a.atttypmod) as type,
           a.attnotnull as not_null,
           pg_get_expr(d.adbin, d.adrelid) as default,
           a.attnum
    from pg_class c
    join pg_namespace n on n.oid = c.relnamespace
    join pg_attribute a on a.attrelid = c.oid and a.attnum > 0 and not a.attisdropped
    left join pg_attrdef d on d.adrelid = c.oid and d.adnum = a.attnum
    where n.nspname in (${SCHEMAS}) and c.relkind = 'r'
    order by schema, c.relname, a.attnum;`);

  const constraints = await q(`
    select n.nspname as schema, cl.relname as table, con.conname as name,
           pg_get_constraintdef(con.oid) as def
    from pg_constraint con
    join pg_class cl on cl.oid = con.conrelid
    join pg_namespace n on n.oid = cl.relnamespace
    where n.nspname in (${SCHEMAS})
    order by 1, 2, con.contype desc, con.conname;`);

  out += section("TABLES");
  const tables = new Map();
  for (const c of cols) {
    const key = `${c.schema}.${c.table}`;
    if (!tables.has(key)) tables.set(key, { rls: c.rls_enabled, cols: [] });
    tables.get(key).cols.push(c);
  }
  for (const [key, t] of tables) {
    out += `\nCREATE TABLE ${key} (\n`;
    out += t.cols
      .map((c) => `  ${c.column} ${c.type}${c.not_null ? " NOT NULL" : ""}${c.default ? ` DEFAULT ${c.default}` : ""}`)
      .join(",\n");
    const cons = constraints.filter((x) => `${x.schema}.${x.table}` === key);
    if (cons.length) out += ",\n" + cons.map((x) => `  CONSTRAINT ${x.name} ${x.def}`).join(",\n");
    out += "\n);\n";
    out += `-- RLS: ${t.rls ? "ENABLED" : "*** DISABLED ***"}\n`;
  }

  // ── Indexes (non-constraint) ───────────────────────────────────────────
  const indexes = await q(`
    select schemaname as schema, tablename as table, indexname as name, indexdef as def
    from pg_indexes
    where schemaname in (${SCHEMAS})
      and indexname not in (select conname from pg_constraint)
    order by 1, 2, 3;`);
  out += section("INDEXES");
  for (const i of indexes) out += `${i.def};\n`;

  // ── RLS policies ───────────────────────────────────────────────────────
  const policies = await q(`
    select schemaname as schema, tablename as table, policyname as name,
           permissive, roles::text as roles, cmd, qual, with_check
    from pg_policies
    where schemaname in (${SCHEMAS})
    order by 1, 2, 3;`);
  out += section("RLS POLICIES");
  for (const p of policies) {
    out += `\nCREATE POLICY "${p.name}" ON ${p.schema}.${p.table}\n  FOR ${p.cmd} TO ${p.roles.replace(/[{}]/g, "")}`;
    if (p.qual) out += `\n  USING (${p.qual})`;
    if (p.with_check) out += `\n  WITH CHECK (${p.with_check})`;
    out += ";\n";
  }

  // ── Views ──────────────────────────────────────────────────────────────
  const views = await q(`
    select schemaname as schema, viewname as name, definition
    from pg_views where schemaname in (${SCHEMAS}) order by 1, 2;`);
  out += section("VIEWS");
  for (const v of views) out += `\nCREATE OR REPLACE VIEW ${v.schema}.${v.name} AS\n${v.definition}\n`;

  // ── Functions ──────────────────────────────────────────────────────────
  const fns = await q(`
    select n.nspname as schema, p.proname as name, pg_get_functiondef(p.oid) as def
    from pg_proc p
    join pg_namespace n on n.oid = p.pronamespace
    where n.nspname in (${SCHEMAS}) and p.prokind = 'f'
      and not exists (select 1 from pg_depend d where d.objid = p.oid and d.deptype = 'e')
    order by 1, 2;`);
  out += section("FUNCTIONS");
  for (const f of fns) out += `\n${f.def};\n`;

  // ── Triggers ───────────────────────────────────────────────────────────
  const trgs = await q(`
    select n.nspname as schema, cl.relname as table, t.tgname as name,
           pg_get_triggerdef(t.oid) as def
    from pg_trigger t
    join pg_class cl on cl.oid = t.tgrelid
    join pg_namespace n on n.oid = cl.relnamespace
    where n.nspname in (${SCHEMAS}) and not t.tgisinternal
    order by 1, 2, 3;`);
  out += section("TRIGGERS");
  for (const t of trgs) out += `${t.def};\n`;

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, out);
  const rlsOff = [...tables.entries()].filter(([, t]) => !t.rls).map(([k]) => k);
  console.log(`Wrote ${path.relative(process.cwd(), OUT)}`);
  console.log(`  ${tables.size} tables, ${policies.length} policies, ${fns.length} functions, ${views.length} views`);
  if (rlsOff.length) {
    console.log(`\n⚠️  RLS DISABLED on ${rlsOff.length} table(s):`);
    for (const t of rlsOff) console.log(`   - ${t}`);
  }
}

main().catch((e) => {
  console.error(e.message);
  process.exit(1);
});
